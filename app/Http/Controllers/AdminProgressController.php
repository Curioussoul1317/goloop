     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $viewparticipants = participants::find($id);
        $progress = Progress::orderBy('created_at', 'desc')->where('event_id', $viewparticipants->cat_event_id)->where('user_id', $viewparticipants->user_id)->get();
        $totalprogress = $progress->sum('event_progress');
        return view('admininvidualprogress')->with('viewparticipants', $viewparticipants)->with('progress', $progress)->with('totalprogress', $totalprogress);
    }

